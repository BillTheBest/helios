package org.helios.containers;

/**
 * <p>Title: TimedOutItemListener</p>
 * <p>Description: Defines a listener that is notified when an item is timed out of a TimeoutQueue</p> 
 * <p>Company: Helios Development Group</p>
 * @author Whitehead (whitehead.nicholas@gmail.com)
 * @version $LastChangedRevision$
 * $HeadURL$
 * $Id$
 */
public interface TimedOutItemListener<E> {
	/**
	 * Fired when an item is timed out of a timeoutqueue.
	 * @param e The item timed out of the queue.
	 * @param tq The queue the item was timed out from.
	 */
	public void itemTimedOut(E e, TimeoutQueue<E> tq);
}
